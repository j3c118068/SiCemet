  <h3 class="acc">Penjelasan</h3>
  <div class="jelas">
    <p>
    Prototyping adalah proses iterative dalam pengembangan sistem dimana requirement diubah ke dalam sistem yang bekerja(working system) yang secara terus menerus diperbaiki melalui kerjasama antara user dan analis. Prototype juga bisa dibangun melalui beberapa tool pengembangan untuk menyederhanakan proses.<br>
    Tahapan model ini adalah : <br>
    1.	Pengumpulan Kebutuhan <br>
    2.	Membangun Prototyping<br>
    3.	Menggunakan Sistem<br>
    4.	Mengkodekan Sistem<br>
    5.	Menguji Sistem<br>
    6.	Evaluasi Sistem<br>
    7.	Evaluasi Prototyping<br>

    </p>
  </div>

  <h3 class="acc">Kelebihan</h3>
  <div class="jelas">
    <p>
    a.	Prototype melibatkan user dalam analisa dan desain.<br>
    b.	Punya kemampuan menangkap requirement secara konkret.<br>
    c.	Digunakan untuk memperluas SDLC.<br>
    </p>
  </div>

  <h3 class="acc">Kelemahan</h3>
  <div class="jelas">
    <p>
    a.	Proses analisis dan perancangan terlalu singkat.<br>
    b.	Mengesampingkan alternatif pemecahan masalah.<br>
    c.	Bisanya kurang fleksible dalam mengahdapi perubahan.<br>
    d.	Protitype yang dihasilkan tidak selamanya mudah dirubah dan cepat selesai. <br>
    </p>
