  <h3 class="acc">Penjelasan</h3>
  <div class="jelas">
    <p>
    RAD adalah penggabungan beberapa metode atau teknik terstruktur. RAD menggunakan metode prototyping dan teknik terstruktur lainnya untuk menentukan kebutuhan user dan perancangan sistem informasiselain itu RAD menekankan siklus perkembangan dalam waktu yang singkat (60 sampai 90 hari) dengan pendekatan konstruksi berbasis komponen. <br>
    Tahapan-tahapan Model RAD <br>
    1.	Bussiness Modelling <br>
    2.	Testing and Turnover<br>
    3.	Aplication Generation<br>
    4.	Process Modelling<br>
    5.	Data Modelling<br>
    </p>
  </div>

  <h3 class="acc">Kelebihan</h3>
  <div class="jelas">
    <p>
    a.	RAD mengikuti tahapan pengembangan sistem sepeti umumnya, tetapi mempunyai kemampuan untuk menggunakan kembali komponen yang ada (reusable object).<br>
    b.	Setiap fungsi dapat dimodulkan dalam waktu tertentu dan dapat dibicarakan oleh tim RAD yang terpisah dan kemudian diintegrasikan sehingga waktunya lebih efesien.<br>
    </p>
  </div>

  <h3 class="acc">Kelemahan</h3>
  <div class="jelas">
    <p>
    a.	Tidak cocok untuk proyek skala besar<br>
    b.	Proyek bisa gagal karena waktu yang disepakati tidak dipenuhi.<br>
    c.	Sistem yang tidak bisa dimodularisasi tidak cocok untuk model ini.<br>
    d.	Resiko teknis yang tinggi juga kurang cocok untuk model ini<br>
    </p>

