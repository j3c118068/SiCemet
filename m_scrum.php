  <h3 class="acc">Penjelasan</h3>
  <div class="jelas">
    <p>
    1. Product Backlog, Ini adalah tahap pertama yang harus kamu lakukan. Product backlogbertujuan untuk menentukan prioritas apa saja yang harus dilakukan selama pengerjaan sprint, yaitu rapat perencanaan pembuatan perangkat lunak yang dilakukan sesuai dengan durasi yang telah disepakati. Product backlogmelibatkan seluruh tim terkait, dari mulai Product Owner, SCRUM Master, hingga tim pengembang.<br><br>

    2. Sprint Backlog, Sprint Backlog adalah kumpulan item Product Backlog yang dipilih untuk Sprint, ditambah sebuah rencana untuk mengantarkan produk tersebut dan mewujudkan tujuan Sprint itu sendiri.<br><br>

    3. Sprint Planning, Sprint planning merupakan proses paling penting yang dilakukan setiap kali akan memulai sprint baru. Dalam proses ini, seluruh tim berkumpul untuk merumuskan tugas apa saja yang ingin dikerjakan dan dirilis dalam beberapa waktu ke depan.<br><br>

    4. Daily Scrum, Di fase ini, masing-masing anggota tim saling berbagi apa saja yang telah dikerjakan, dan apa yang akan dikerjakan di hari tersebut. Para anggota tim juga dapat melaporkan hambatan yang ditemui selama pengerjaan. Daily SCRUM dilakukan setiap hari selama sprint berlangsung.<br><br>

    5. Sprint Review, Sprint review adalah waktunya bagi anggota tim untuk mendemonstrasikan apa saja yang berhasil diselesaikan dalam satu sprint. Sprint review dilakukan setelah satu kali pengerjaan sprint selesai.<br><br>

    6. Sprint Retrospective, Sprint retrospective dilakukan di akhir setiap sprint. Dalam meeting ini, masing-masing anggota tim (termasuk SCRUM Master dan Product Owner) mengutarakan pendapat terkait kinerja tim selama menerapkan SCRUM tanpa membahas sisi teknis dari proyek yang telah dikerjakan.<br><br>

    7. Increment, Manifestasi dari product backlog item yang diselesaikan dalam sprint dan total nilai bisnis increment dan seluruh sprint yang lalu.<br><br>
    </p>
  </div>

  <h3 class="acc">Kelebihan</h3>
  <div class="jelas">
    <p>
    1.	Keperluan berubah dengan cepat<br>
    2.	Tim berukuran kecil sehingga melancarkan komunikasi, mengurangi biaya dan memberdayakan satu sama lain<br>
    3.	Pekerjaan terbagi-bagi sehingga dapat diselesaikan dengan cepat<br>
    4.	Dokumentasi dan pengujian terus menerus dilakukan setelah software dibangun<br>
    5.	Proses Scrum mampu menyatakan bahwa produk selesai kapanpun diperlukan<br>
    </p>
