  <h3 class="acc">Penjelasan</h3>
  <div class="jelas">
    <p>
    Fungsi model spiral adalah untuk melakukan perubahan, penambahan dan pengembangan perangkat lunak dengan memaksimalkan aspek kecepatan dan ketepatan berdasarkan keinginan dan kebutuhan penggunanya.<br><br>

    Tahapan :<br>
    1.	Tahap Liason, Tahap ini berhubungan dengan komunikasi antara pihak-pihak yang terlibat dalam pengembangan softaware (seperti: system analyst) dengan pelanggan (user). Tujuannya adalah memperbaiki dan mengembangan software sesuai kebutuhan dan keinginan hingga memuaskan pelanggan.<br><br>
    2.	Tahap planning
    Tahap perencanaan meliputi estimasi biaya yang digunakan, batas waktu, pengaturan jadwal, identifikasi lingkungan kerja, sumber-sumber informasi untuk melakukan iterasi (Teknik perulangan). Hasil dari tahapan ini adalah dokumen spesifikasi kebutuhan sistem dan bisnis.<br><br>
    3.	Tahap analisis risiko
    Tahap analisis resiko berfungsi untuk mengidentifikasi resiko yang berpotensi akan terjadi dan menghasilkan solusi alternative.<br><br>

    4.	Tahapan rekayasa (engineering)
    Pada tahap rekayasa, beberapa kegiatan ini yang akan dilakukan, yaitu:<br>
    •	Menguji, coding dan mengembangkan software<br>
    •	Menginstal software<br>
    •	Membuat prototype<br>
    •	Mendesain dokumen<br>
    •	Meringkas suatu pengujian software<br>
    •	Membuat laporan atas kekurangan dari software agar segera diperbaiki<br><br>
    5.	Tahap evaluasi
    Pada tahap evaluasi, system analyst membutuhkan masukan dan tanggapan dari para user dalam mengevaluasi perangkat/produk yang diuji dan memastikan bahwa produk dibutuhkan sesuai ketentuan yang telah dibicarakan diawal dengan user.

    </p>
  </div>

  <h3 class="acc">Kelebihan</h3>
  <div class="jelas">
    <p>
    1.	Pembangunan dan perubahan perangkat lunak yang terjadi dapat diselesaikan secara sistematis<br>
    2.	Mudah dalam mengestimasi biaya karena proses pembuatan prototype yang jelas dan terencana dalam tahapan yang sistematis<br>
    3.	Manajemen dan analisa risiko yang lebih cepat dan mudah<br>
    4.	Mudah dalam melakukan perubahan kebutuhan dan dokumentasi<br>
    5.	Produksi software bisa terjadi lebih cepat<br>

    </p>
  </div>

  <h3 class="acc">Kelemahan</h3>
  <div class="jelas">
    <p>
    1.	Tidak cocok dan sulit diimplementasikan dalam projek kecil<br>
    2.	Memakan waktu yang cukup lama<br>
    3.	Membutuhkan best practice atau pengalaman sebelumnya karena proses yang sangat kompleks<br>
    4.	Resiko dalam tahap planning cukup besar. Misalnya terjadi perbedaan dalam jadwal pengembangan dan anggaran belanja.<br>

    </p>
