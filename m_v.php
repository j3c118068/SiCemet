  <h3 class="acc">Penjelasan</h3>
  <div class="jelas">
    <p>
    <b>Business Requirement Analysis</b>, Ini adalah tahap pertama dalam siklus pengembangan di mana persyaratan produk dipahami dari perspektif pelanggan. Fase ini melibatkan komunikasi rinci dengan pelanggan untuk memahami harapan dan kebutuhan yang tepat. Ini merupakan kegiatan yang sangat penting dan perlu dikelola dengan baik, karena sebagian besar pelanggan tidak yakin tentang apa yang sebenarnya mereka butuhkan Acceptance test desain dilakukan pada tahap ini sebagai kebutuhan bisnis dapat digunakan sebagai masukan untuk pengujian penerimaan. <br><br>

    <b>System Design</b>, Setelah Anda memiliki persyaratan produk yang jelas dan rinci, sekarang saatnya untuk merancang sistem yang lengkap. Desain sistem akan memiliki pemahaman dan merinci hardware lengkap dan setup komunikasi untuk produk dalam pengembangan. Rencana pengujian sistem dikembangkan berdasarkan desain sistem. Melakukan hal ini pada tahap awal membuat lebih banyak waktu untuk pelaksanaan tes yang sebenarnya nanti.<br><br>

    <b>Architectural Design</b>, spesifikasi arsitektur dipahami dan dirancang dalam fase ini. Biasanya lebih dari satu pendekatan teknis diusulkan dan berdasarkan kelayakan teknis dan finansial keputusan akhir diambil. Desain sistem dipecah lebih jauh ke dalam modul mengambil fungsi yang berbeda. Hal ini juga disebut sebagai "Desain Tingkat Tinggi" 
    Module Design, Pada fase ini, desain internal rinci untuk semua modul sistem yang ditentukan, disebut "Desain Tingkat Rendah". Penting  bahwa desain tersebut kompatibel dengan modul lain dalam arsitektur sistem dan sistem eksternal lainnya.<br>

    <b>Coding Phase</b>, Bahasa pemrograman yang paling cocok ditentukan berdasarkan sistem dan persyaratan arsitektur. pengkodean dilakukan berdasarkan pedoman coding dan standar. Kode berjalan melalui berbagai ulasan kode dan dioptimalkan untuk kinerja terbaik sebelum final membangun diperiksa ke dalam repositori<br><br>

    <b>Unit Testing</b>, adalah pengujian pada tingkat kode dan membantu menghilangkan bug pada tahap awal, meskipun semua cacat tidak dapat ditemukan oleh unit testing.<br><br>

    <b>Integration Testing</b>, dikaitkan dengan fase desain arsitektur. tes integrasi dilakukan untuk menguji koeksistensi dan komunikasi dari modul internal dalam sistem.<br><br>

    <b>System Testing</b>, secara langsung berhubungan dengan tahap desain sistem. System testing memeriksa seluruh fungsi sistem dan komunikasi sistem dalam pengembangan dengan sistem eksternal. Sebagian besar perangkat lunak dan perangkat keras masalah kompatibilitas dapat ditemukan selama pelaksanaan test ini.<br><br>

    <b>Acceptance Testing</b>, dikaitkan dengan tahap analisis kebutuhan bisnis dan melibatkan pengujian produk di lingkungan pengguna.  Acceptance testing mengungkap masalah kompatibilitas dengan sistem lain yang tersedia di lingkungan pengguna. Juga menemukan masalah non-fungsional seperti beban dan kinerja cacat pada aktual lingkungan pengguna.<br><br>

    </p>
  </div>

  <h3 class="acc">Kelebihan</h3>
  <div class="jelas">
    <p>
    1.	Ini adalah model yang sangat-disiplin dan Tahapan selesai satu per satu.  <br>
    2.	Bekerja dengan baik untuk proyek-proyek yang lebih kecil dimana persyaratan dipahami dengan baik.<br>
    3.	Sederhana dan mudah dimengerti dan digunakan.<br>
    4.	Mudah dikelola karena setiap fase memiliki spesifik kiriman dan proses review.<br>
    </p>
  </div>

  <h3 class="acc">Kelemahan</h3>
  <div class="jelas">
    <p>
    1.	Berisiko tinggi dan ketidakpastian.<br>
    2.	Tidak cocok untuk proyek-proyek yang kompleks dan berorientasi objek.<br>
    3.	Tidak cocok untuk proyek-proyek dimana persyaratan beresiko tinggi<br>
    4.	Tidak cocok untuk proyek-proyek yang lama dan berkelanjutan.<br>
    5.	Setelah aplikasi dalam tahap pengujian, sulit untuk kembali dan mengubah fungsionalitas.<br>
    </p>
