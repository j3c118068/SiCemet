  <h3 class="acc">Penjelasan</h3>
  <div class="jelas">
    <p>
    XP adalah sebuah proses perangkat lunak yang membantu pengembang membuat kode berkualitas dengan cepat. Di sini, kita mendefinisikan kualitas sebagai sebuah basis kode yang sesuai dengan desain spesifikasi dan ekspektasi pelanggan.<br><br>

Model Pengembangan XP memiliki 12 praktik inti yang mengarahkan proses, dirangkum pada tabel 1. Singkatnya, Anda dapat mengelompokkan 12 praktik inti XP ke dalam 4 konsep:<br>
1.	Mendengarkan pelanggan dan pemrogram lain<br>
2.	Kolaborasi dengan pelanggan untuk mengembangkan spesifikasi aplikasi dan kasus pengujian<br>
3.	Koding dengan seorang rekan pemrograman<br>
4.	Pengujian dan pengujian kembali basis kode<br>


    </p>
  </div>

  <h3 class="acc">Kelebihan</h3>
  <div class="jelas">
    <p>
    •	Meningkatkan kepuasan kepada klien<br>
    •	Pembangunan system dibuat lebih cepat<br>
    •	Menjalin komunikasi yang baik dengan client.<br>
    •	Meningkatkan komunikasi dan sifat saling menghargai antar developer.<br>


        </p>
  </div>

  <h3 class="acc">Kelemahan</h3>
  <div class="jelas">
    <p>
    •	Cerita-cerita yang menunjukkan requirements dari pelanggan kemungkinan besar tidak lengkap sehingga Developer harus selalu siap dengan perubahan karena perubahan akan selalu diterima.<br>
    •	Tidak bisa membuat kode yang detail di awal (prinsip simplicity dan juga anjuran untuk melakukan apa yang diperlukan hari itu juga).<br>
    •	XP tidak memiliki dokumentasi formal yang dibuat selama pengembangan. Satu-satunya dokumentasi adalah dokumentasi awal yang dilakukan oleh user.<br>


    </p>

